package com.example.myshh.sensorcharts;

class BooleanHolder {
    private boolean booleanValue;

    BooleanHolder() {
        this.booleanValue = false;
    }

    boolean isBooleanValue() {
        return booleanValue;
    }

    void setBooleanValue(boolean booleanValue) {
        this.booleanValue = booleanValue;
    }
}
